package com.must.courseevaluation.service.agent;

/**
 * 用户意图类型枚举
 */
public enum IntentType {
    /**
     * 新查询 - 全新的课程推荐请求
     * 示例："推荐编程课程"、"有什么医学课程"
     */
    NEW_QUERY,
    
    /**
     * 追问筛选 - 从上次推荐中筛选/细化
     * 示例："上述课程中有神经网络的吗"、"刚才推荐的哪个更简单"
     */
    REFINE,
    
    /**
     * 条件补充 - 补充/修改之前的条件
     * 示例："要选修课"、"3学分的"、"简单点的"
     */
    SUPPLEMENT,
    
    /**
     * 课程比较 - 比较多门课程
     * 示例："这两门课哪个更好"、"数据库和算法课有什么区别"
     */
    COMPARE,
    
    /**
     * 课程详情 - 询问特定课程的详细信息
     * 示例："人工智能导论讲什么"、"这门课难吗"
     */
    DETAIL,
    
    /**
     * 闲聊 - 非课程推荐相关的对话
     * 示例："谢谢"、"你好"、"再见"
     */
    CHAT
}

